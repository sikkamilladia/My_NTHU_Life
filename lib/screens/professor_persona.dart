        ? Colors.green
        : _cognitiveSyncLevel >= 45
            ? Colors.amber
            : Colors.redAccent;

    return Row(
      children: [
        // Real-Time Telemetry Sidebar (Interactive Multi-Turn Diagnostics)
        Container(
          width: 120,
          padding: const EdgeInsets.all(12),
          decoration: BoxDecoration(
            color: cs.surfaceContainerLow,
            border: Border(right: BorderSide(color: cs.outlineVariant, width: 1)),
