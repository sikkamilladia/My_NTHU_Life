class ProfessorPersonaScreen extends StatefulWidget {
  final String studentID;
  const ProfessorPersonaScreen({super.key, required this.studentID});

  @override
  State<ProfessorPersonaScreen> createState() => _ProfessorPersonaScreenState();
}

class _ProfessorPersonaScreenState extends State<ProfessorPersonaScreen> {
  final TextEditingController _projectNameController = TextEditingController(text: "Smart Campus Companion");
  final TextEditingController _courseNameController = TextEditingController(text: "Software Studio");
  final TextEditingController _docController = TextEditingController();
  final TextEditingController _chatInputController = TextEditingController();
  final ScrollController _chatScrollController = ScrollController();

  String _selectedProfessor = "Prof. Wang (The Socratic Sage)";
  final List<String> _professors = [
    "Prof. Wang (The Socratic Sage)",
    "Prof. Lin (The Pragmatic Veteran)"
  ];

  // Ingestion Phase States
  bool _isAutoGeneratingDoc = false;
  bool _isStartingDefense = false;

  // Active Presentation States
  bool _isDefenseActive = false;
  List<Map<String, String>> _chatHistory = []; // role: 'professor' or 'student', content: '...'
  bool _isProfessorThinking = false;

  // Real-time Diagnostic Telemetry States
  int _cognitiveSyncLevel = 50; // starts at 50, goes up/down
  List<String> _detectedWeaknesses = [];
  int _turnsCount = 0;

  // Final Report Card States
  bool _isGrading = false;
  Map<String, dynamic>? _gradeReport; // final grades from AIService

  @override
  void dispose() {
    _projectNameController.dispose();
    _courseNameController.dispose();
    _docController.dispose();
    _chatInputController.dispose();
    _chatScrollController.dispose();
    super.dispose();
  }

  Future<void> _autoGenerateDoc() async {
    final proj = _projectNameController.text.trim();
    final course = _courseNameController.text.trim();
    if (proj.isEmpty || course.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text("Please fill Project Name and Course Name first!"),
          backgroundColor: Colors.amber,
        ),
      );
      return;
    }

    setState(() => _isAutoGeneratingDoc = true);
    try {
      final res = await AIService.generateProjectDocMock(projectName: proj, courseName: course);
      if (res.containsKey('documentation')) {
        setState(() {
          _docController.text = res['documentation'] ?? "";
        });
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text("✨ Mock technical documentation compiled for $proj!"),
            backgroundColor: Colors.purpleAccent,
          ),
        );
      }
    } catch (e) {
      print("Error autogenerating doc: $e");
    } finally {
      setState(() => _isAutoGeneratingDoc = false);
    }
  }

  Future<void> _initiateDefense() async {
    final proj = _projectNameController.text.trim();
    final course = _courseNameController.text.trim();
    final docText = _docController.text.trim();

    if (proj.isEmpty || course.isEmpty || docText.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text("All fields (Project, Course, and Documentation) are required to feed the professor!"),
          backgroundColor: Colors.amber,
        ),
      );
      return;
    }

    setState(() => _isStartingDefense = true);
    try {
      final res = await AIService.startProfessorInterview(
        projectName: proj,
        courseName: course,
        projectDocText: docText,
        professorStyle: _selectedProfessor,
      );

      if (res.containsKey('question')) {
        setState(() {
          _chatHistory = [
            {'role': 'professor', 'content': res['question'] ?? ""}
          ];
          _cognitiveSyncLevel = res['score_progress'] ?? 50;
          _detectedWeaknesses = [res['detected_weakness'] ?? "Initial defense launched"];
          _turnsCount = 1;
          _isDefenseActive = true;
        });
      }
    } catch (e) {
      print("Error starting professor defense: $e");
    } finally {
      setState(() => _isStartingDefense = false);
    }
  }

  Future<void> _submitResponse() async {
    final responseText = _chatInputController.text.trim();
    if (responseText.isEmpty || _isProfessorThinking) return;

    setState(() {
      _chatHistory.add({'role': 'student', 'content': responseText});
      _chatInputController.clear();
      _isProfessorThinking = true;
    });

    _scrollToBottom();

    try {
      final res = await AIService.evaluateAndGenerateNextQuestion(
        projectName: _projectNameController.text.trim(),
        courseName: _courseNameController.text.trim(),
        projectDocText: _docController.text.trim(),
        professorStyle: _selectedProfessor,
        chatHistory: _chatHistory,
      );

      if (res.containsKey('next_question')) {
        setState(() {
          _chatHistory.add({'role': 'professor', 'content': res['next_question'] ?? ""});
          
          final scoreDelta = res['score_delta'] as int? ?? 0;
          _cognitiveSyncLevel = (_cognitiveSyncLevel + scoreDelta).clamp(0, 100);

          final weakness = res['detected_weakness'] as String? ?? "";
          if (weakness.isNotEmpty && !_detectedWeaknesses.contains(weakness)) {
            _detectedWeaknesses.add(weakness);
          }
          
          _turnsCount++;
          _isProfessorThinking = false;
        });
        _scrollToBottom();
      }
    } catch (e) {
      print("Error responding in professor interview: $e");
      setState(() => _isProfessorThinking = false);
    }
  }

  Future<void> _concludeDefense() async {
    final confirm = await showDialog<bool>(
      context: context,
      builder: (context) => AlertDialog(
        backgroundColor: Theme.of(context).colorScheme.surfaceContainerHigh,
        title: Text(
          "CONCLUDE ORAL EXAM",
          style: GoogleFonts.orbitron(
            fontSize: 14,
            fontWeight: FontWeight.bold,
            letterSpacing: 1.2,
          ),
        ),
        content: const Text("Are you ready to submit your transcript and request formal grading from the faculty professor?"),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(context, false),
            child: const Text("CANCEL"),
          ),
          TextButton(
            onPressed: () => Navigator.pop(context, true),
            child: const Text("SUBMIT FOR GRADING"),
          ),
        ],
      ),
    );

    if (confirm != true) return;

    setState(() => _isGrading = true);

    try {
      final res = await AIService.generateFinalGradeReport(
        projectName: _projectNameController.text.trim(),
        courseName: _courseNameController.text.trim(),
        professorStyle: _selectedProfessor,
        chatHistory: _chatHistory,
      );

      if (res.containsKey('grade')) {
        setState(() {
          _gradeReport = res;
          _isGrading = false;
        });

        final bool passed = res['passed'] as bool? ?? false;
        if (passed) {
          // Award Gamified growth points to pet & user
          Provider.of<PetProvider>(context, listen: false)
              .awardGrowthPoints(studentID: widget.studentID, exp: 30, coins: 15);

          await FirebaseFirestore.instance
              .collection('users')
              .doc(widget.studentID)
              .set({
                'weeklyXP': FieldValue.increment(30),
              }, SetOptions(merge: true));
        }
      }
    } catch (e) {
      print("Error during grade generation: $e");
      setState(() => _isGrading = false);
    }
  }

  void _scrollToBottom() {
    WidgetsBinding.instance.addPostFrameCallback((_) {
      if (_chatScrollController.hasClients) {
        _chatScrollController.animateTo(
          _chatScrollController.position.maxScrollExtent,
          duration: const Duration(milliseconds: 300),
          curve: Curves.easeOut,
        );
      }
    });
  }

  void _resetSession() {
    setState(() {
      _isDefenseActive = false;
      _chatHistory = [];
      _cognitiveSyncLevel = 50;
      _detectedWeaknesses = [];
      _turnsCount = 0;
      _gradeReport = null;
      _docController.clear();
    });
  }

  @override
  Widget build(BuildContext context) {
    final cs = Theme.of(context).colorScheme;

    if (_isStartingDefense) {
      return _buildLoadingScreen(cs, "LAUNCHING INTERACTIVE SIMULATOR", "Professor is establishing the cognitive sync link and preparing final oral defense telemetry...");
    }

    if (_isGrading) {
      return _buildLoadingScreen(cs, "COMPILING ACADEMIC REVIEW", "Faculty is evaluating the dialogue transcript and preparing the formal university Grade Report...");
    }

    if (_gradeReport != null) {
      return _buildGradeReportCard(cs);
    }

    return Scaffold(
      backgroundColor: cs.surface,
      appBar: AppBar(
        backgroundColor: cs.surface,
        elevation: 0,
        leading: IconButton(
          icon: Icon(Icons.arrow_back_ios_new_rounded, color: cs.onSurface),
          onPressed: () => Navigator.pop(context),
        ),
        title: Text(
          "PROFESSOR PERSONA",
          style: GoogleFonts.orbitron(
            fontSize: 15,
            fontWeight: FontWeight.bold,
            color: cs.onSurface,
            letterSpacing: 1.5,
          ),
        ),
        centerTitle: true,
      ),
      body: SafeArea(
        child: _isDefenseActive ? _buildInterviewRoom(cs) : _buildConfigForm(cs),
      ),
    );
  }

